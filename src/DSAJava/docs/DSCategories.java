package DSAJava.docs;

public class DSCategories {
    /*
     * Data Structures are divided into three parts.
     * Linear, Non-linear, and Hashed.
     * Linear Data Structures:
     *      Are store data in some sort of sequential order where each element connects
     *      to its previous and next element.
     *  Arrays: Fixed size contigous memory storing element of the same type.
     *  Linked Lists: Elements (nodes) containing data and pointers to the next node.
     *  Stacks: Use a principle of LIFO(Last-in, First-out) where the insertion and deletions
     *          of the elements happen at one end.
     *  Queues: FIFO(First-in, First-out) where items(element) insert at the back and exit front.
     *  Vectors/Dynamic Arrays: Arrays that are dynamically resize when elements are added.
     *
     * Non-Linear Data Structure:
     *      Elements are not arranged sequentially, but in the form of heirarchical, or multi level
     *      relationships.
     *  Trees: Hierarchical structures with a root node and parent-child relationships
     *          (e.g., Binary Trees, AVL Trees, Heaps).
     *  Graphs: Networks consisting of vertices (nodes) connected by edges (links),
     *          which can be directed or undirected.
     *  Tries: Prefix trees used for efficient retrieval of keys, typically strings.
     *  Multidimensional Arrays: Matrix structures (like 2D arrays)
     *          representing data in grids rather than a single line.
     * Hashed Data Structures:
     *      Elements are stored and retrieved out of order using a hash function to transform a key into
     *      an index location for ultra-fast, constant-time access.
     *  Hash Tables: Key-value pairs mapped to array indices via a hashing algorithm.
     *  Hash Maps: Object-oriented implementations of hash tables mapping unique keys to values.
     *  Hash Sets: Collections of unique elements backed internally by a hashing mechanism.
     *  Bloom Filters: Space-efficient, probabilistic data structures used to test set membership.
     */
}
